(GNT_WH). A digitalizált szöveget <a href="http://www.ehf.hu/ujszov/letolthetok/GNT_WH_e_book.zip">e-bookba</a>
szerveztük, amelyet a liktárunkból bárki letölthet, és összevethet a könyvkiadás
<a href="http://clerus.hu/sites/clerus.hu/files/ujszov/scan/ls.html">beszkennelt
