<p>Hiányzó részek, további tervek:</p>


<p>Föltett szándékunk, hogy hárman készítjük el a hiányzó részek szószedeteit,
ugyanis nem tenne jót a munka egységes stílusának, ha ebbe új kollégákat vonnánk be.
Annál szívesebben veszünk minden kritikai észrevételt, akár alkalmit, akár egy-egy
fejzet rendszeres korrektúráját. Ebbe az Esztergomi Hittudományi Főiskola
növendékeitől kezdve minden fölhasználó bekapcsolódását szívesen fogadjuk.</p>

<p>További terveinket meghatározza, hogy mire találunk munkatársakat: lehetséges pl.
klasszikus görög szövegek, egyházatyák, vagy a Septuaginta egyes részeinek hasonló
stílusú preparációja.  Kézenfekvőek pl. a Deuterokanonikus könyvek, ha azok
elkészítésére vállalkozik valaki, azt nagy örömmel kapcsolnánk a munkánkhoz.</p>

<p>Mindezekben bárki segítségét, észrevételeit szívesen vesszük.</p>

<p>A héber Ószövetség preparációjának útjában komoly technikai akadály, hogy a
kevert, héber- és másnyelvű írás a számítógépen alapjában megoldatlan. Ennek ellenére
van egy Excelben kipreparált Jónás könyvem, amennyiben azt sikerül közzétennem, az
jelezni fogja, hogy a szószedet kiterjeszthető majd az Ószövetségre is.</p>


<p>Már a szószedet elkészítése kapcsán gyakran keletkeztek közöttünk termékeny viták,
amelyekből lábjegyzetek születtek. Adódik a lehetőség, hogy a Szentírás egyes
részeihez bibliográfiát kapcsoljunk, amely segít abban, hogy a szakirodalomban hol
lehet utánanézni egy-egy szöveg pontos értelmének.</p>

<p>Hosszú távú célunk mindezt egy Interneten, vagy akár nyomtatásban is kiadott
magyar nyelvű biblikus folyóirattá fejleszteni.</p>

<p>Munkamódszerünk:</p>

<p>Egy-egy szó egy-egy konkrét alakjához csak egy esetben írunk kézzel jelentést és
elmzést, a többi azonos szóalakhoz a gép másolja az egy helyre kézzel beírt
információt. Így nem kell pl. több, mint 9000-szer leírni a "kai"-szó jelentését és
